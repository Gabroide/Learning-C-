//: C03:Autoincrement.cpp
// Shows use of auto-incremetn
// andayto-decrement operators.
#include <iostream>
using namespace std;

int main()
{
	int i = 0;
	int j = 0;
	cout << ++i << endl; // Pre-increment
	cout << j++ << endl; // Post-increment
	cout << --i << endl; // Pre-decrement
	cout << j-- << endl; // Post-decrement
}///:~2
